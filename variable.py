a=int(input("first number"))                                                                          
b=int(input("second number"))
c=input("symbol")

if c=="-":
    print(a-b)
    print("substration")
elif c=="+":
    print(a+b)
    print("addition")
elif c=="*":
    print(a*b)
    print("multiplication")
elif c=="/":
    print(a/b)
    print("division")

